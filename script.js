let activeStoryKey = null;
let activeSkillKey = null;

function renderList(target, items) {
  target.innerHTML = "";
  items.forEach((item) => {
    const li = document.createElement("li");
    li.textContent = item;
    target.appendChild(li);
  });
}

function positionStoryPanel(trigger) {
  if (!trigger) return;

  const worldRect = document.querySelector(".mountain-world").getBoundingClientRect();
  const triggerRect = trigger.getBoundingClientRect();
  const triggerCenterX = triggerRect.left + triggerRect.width / 2;
  const triggerCenterY = triggerRect.top + triggerRect.height / 2;
  const worldCenterX = worldRect.left + worldRect.width / 2;
  const worldCenterY = worldRect.top + worldRect.height / 2;

  storyPanel.classList.remove("dock-left", "dock-right", "dock-top", "dock-bottom");
  storyPanel.classList.add(triggerCenterX < worldCenterX ? "dock-right" : "dock-left");
  storyPanel.classList.add(triggerCenterY < worldCenterY ? "dock-bottom" : "dock-top");
}

function closeStoryPanel() {
  storyPanel.classList.add("is-hidden");
  activeStoryKey = null;
  campButtons.forEach((button) => button.classList.remove("active"));
}

function activateCamp(key, trigger = null) {
  const content = storyContent[key];
  if (!content) return;

  if (activeStoryKey === key && !storyPanel.classList.contains("is-hidden")) {
    closeStoryPanel();
    return;
  }

  panelKicker.textContent = content.kicker;
  panelTitle.textContent = content.title;
  panelText.textContent = content.text;
  panelActionsTitle.textContent = content.actionsTitle || "Wat moet hier gebeuren?";
  panelWhyTitle.textContent = content.whyTitle || "Waarom is dit belangrijk?";
  panelRolesTitle.textContent = content.rolesTitle || "Wie zijn hier belangrijk?";
  panelRiskTitle.textContent = content.riskTitle || "Wat gaat mis als dit ontbreekt?";
  panelExampleTitle.textContent = content.exampleTitle || "Voorbeeld";
  renderList(panelActions, content.actions);
  renderList(panelWhy, content.why);
  renderList(panelRoles, content.roles);
  panelRisk.textContent = content.risk;
  panelExample.textContent = content.example || "";
  panelExampleSection.hidden = !content.example;

  campButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.camp === key);
  });

  storyPanel.classList.remove("is-hidden");
  activeStoryKey = key;
  positionStoryPanel(trigger || document.querySelector(`[data-camp="${key}"], [data-open-panel="${key}"]`));
}

function closeSkillPanel() {
  skillPanel.classList.add("is-hidden");
  activeSkillKey = null;
  skillButtons.forEach((button) => button.classList.remove("active"));
}

function activateSkill(key) {
  const content = roleSkills[key];
  if (!content) return;

  if (activeSkillKey === key && !skillPanel.classList.contains("is-hidden")) {
    closeSkillPanel();
    return;
  }

  skillKicker.textContent = content.kicker;
  skillTitle.textContent = content.title;
  skillText.textContent = content.text;
  renderList(skillList, content.skills);
  skillNote.textContent = content.note;

  skillButtons.forEach((button) => {
    button.classList.toggle("active", button.dataset.skill === key);
  });

  skillPanel.classList.remove("is-hidden");
  activeSkillKey = key;
}

campButtons.forEach((button) => {
  button.addEventListener("click", () => activateCamp(button.dataset.camp, button));
});

openButtons.forEach((button) => {
  button.addEventListener("click", () => activateCamp(button.dataset.openPanel, button));
});

navButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const key = button.dataset.campTarget;
    activateCamp(key, document.querySelector(`[data-camp="${key}"]`));
    document.querySelector(`[data-camp="${key}"]`)?.scrollIntoView({
      behavior: "smooth",
      block: "center",
      inline: "center"
    });
  });
});

skillButtons.forEach((button) => {
  button.addEventListener("click", () => activateSkill(button.dataset.skill));
});

closeButtons.forEach((button) => {
  button.addEventListener("click", () => {
    if (button.dataset.closePanel === "story") closeStoryPanel();
    if (button.dataset.closePanel === "skill") closeSkillPanel();
  });
});

